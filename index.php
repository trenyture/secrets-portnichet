<?php 

	/************************************************************
	**************************  HEADER  *************************
	************************************************************/
	get_template_part( 'parts/header' );
	
	/************************************************************
	************************  TOP MENU  *************************
	************************************************************/
	if (!is_front_page()){
		get_template_part('parts/top_menu');
	}

	if (is_front_page()){
		/************************************************************
		*************************  HOME  ****************************
		************************************************************/ 
		get_template_part('parts/home');
	}
	else {
		/************************************************************
		*************************  CONTENT  *************************
		************************************************************/ 
		if ( have_posts() ) : while ( have_posts() ) : the_post(); ?>
			<main>
				<?php the_content(); ?>
			</main>
		<?php endwhile; endif;
	}


	/************************************************************
	**************************  FOOTER  *************************
	************************************************************/
	get_template_part( 'parts/footer' ); 

?>